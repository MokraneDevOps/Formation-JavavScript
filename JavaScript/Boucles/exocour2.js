for (let i = 0; i <= 100; i += 2) {
    console.log(i);
  }
  

  // Explication :
// let i = 0 : On initialise la variable i à 0 (le premier nombre pair).
// i <= 100 : La boucle continue tant que i est inférieur ou égal à 100.
// i += 2 : On incrémente i de 2 à chaque itération, ce qui fait que seuls les nombres pairs seront affichés.
// console.log(i) : Affiche la valeur actuelle de i à chaque tour de boucle.