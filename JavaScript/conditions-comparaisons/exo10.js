const fruit = 'banane'; // Déclaration et assignation de la variable fruit

// Début de votre code
if (fruit === "banane") {
  console.log("Ce fruit est une banane"); // Affiche ce message si fruit est égal à "banane"
} else {
  console.log("Je ne connais pas ce fruit"); // Affiche ce message dans tous les autres cas
}
